    private void jBuploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBuploadActionPerformed
      ImageIcon format = null;
        String filename;
        JFileChooser jchooser = new JFileChooser();
        jchooser.showOpenDialog(null);
        File f = jchooser.getSelectedFile();
        filename = f.getAbsolutePath();
        Jtpath.setText(filename);

        try {
            File image = new File(filename);
            FileInputStream fis = new FileInputStream(image);

            ByteArrayOutputStream bos = new ByteArrayOutputStream();
            byte[] buf = new byte[1024];
            for (int readNum; (readNum = fis.read(buf)) != -1;) {
                bos.write(buf, 0, readNum);
            }
            byte[] person_image = bos.toByteArray();
            format = new ImageIcon(person_image);
            jLabel1.setIcon(format);
            //jDesktopPane1.add(format);
            //conn.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_jBuploadActionPerformed

